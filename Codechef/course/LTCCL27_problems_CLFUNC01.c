// Functions in C
// Write a program to calculate the square and cube of numbers 2, 3, and 4.

//ANS

#include <stdio.h>

int main(void) {
	int a = 2;
	int b = 3;
	int c = 4;
	printf("%d\n", a*a);
    printf("%d\n", a*a*a);
    
	printf("%d\n", b*b);
    printf("%d\n", b*b*b);
    
	printf("%d\n", c*c);
	printf("%d\n", c*c*c);
    return 0;
}
